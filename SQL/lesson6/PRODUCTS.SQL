CREATE TABLE SHIPPERS
(
    ID NUMBER,
    CONSTRAINT SHIPPERS_ID PRIMARY KEY (ID),
    COMPANY_NAME NVARCHAR2(50) NOT NULL
);

CREATE TABLE CUSTOMERS
(
    ID NUMBER,
    CONSTRAINT CUSTOMERS_ID PRIMARY KEY (ID),
    COMPANY_NAME NVARCHAR2(50) NOT NULL,
    CONTACT_NAME NVARCHAR2(50),
    CONTACT_TITLE NVARCHAR2(100),
    ADDRESS NVARCHAR2(50),
    CITY NVARCHAR2(50),
    REGION NVARCHAR2(50),
    POSTAL_CODE NUMBER(5)
    COUNTRY NVARCHAR2(50),
    PHONE NUMBER,
    FAX NUMBER
);

CREATE TABLE EMPLOYEES
(
    ID NUMBER,
    CONSTRAINT EMPLOYEE_ID PRIMARY KEY (ID),
    LAST_NAME NVARCHAR2(50) NOT NULL,
    FIRST_NAME NVARCHAR2(50) NOT NULL,
    TITLE NVARCHAR2(50),
    TITLE_OF_COURTESY NVARCHAR2(50),
    BIRTH_DATE DATE,
    HIRE_DATE DATE,
    ADDRESS NVARCHAR2(50),
    CITY NVARCHAR2(50),
    REGION NVARCHAR2(50),
    POSTAL_CODE NUMBER(5,)
    COUNTRY NVARCHAR2(50),
    HOME_PHONE NUMBER,
    EXTENSION NVARCHAR2(50),
    PHOTO NVARCHAR2(50),
    NOTES NVARCHAR2(100),
    REPOST_TO NVARCHAR2(50)
);

CREATE TABLE ORDERS
(
    ID NUMBER,
    CONSTRAINT ORDERS_ID PRIMARY KEY (ID),
    CUSTOMER_ID NUMBER,
    CONSTRAINT CUSTOMER_FK FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS (ID),
    EMPLOYEE_ID NUMBER,
    CONSTRAINT EMPLOYEE_FK FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES (ID),
    ORDER_DATE TIMESTAMP,
    REQUIRED_DATE TIMESTAMP,
    SHIPPED_DATE TIMESTAMP,
    SHIP_VIA NVARCHAR2(50),
    SHIP_NAME NVARCHAR2(50),
    SHIP_ADDRESS NVARCHAR2(50),
    SHIP_CITY NVARCHAR2(50),
    SHIP_REGION NVARCHAR2(50),
    SHIP_POSTAL_CODE NUMBER(5),
    SHIP_COUNTRY NVARCHAR2(50)
);

CREATE TABLE SUPPLIERS
(
    ID NUMBER,
    CONSTRAINT SUPPLIER_ID PRIMARY KEY (ID),
    COMPANY_NAME NVARCHAR2(50) NOT NULL,
    CONTACT_NAME NVARCHAR2(50),
    CONTACT_TITLE NVARCHAR2(100),
    ADDRESS NVARCHAR2(50),
    CITY NVARCHAR2(50),
    REGION NVARCHAR2(50),
    POSTAL_CODE NUMBER(5,)
    COUNTRY NVARCHAR2(50),
    PHONE NUMBER,
    FAX NUMBER,
    HOME_PAGE NVARCHAR2(50)
);

CREATE TABLE CATEGORIES
(
    ID NUMBER,
    CONSTRAINT CATEGORY_ID PRIMARY KEY (ID),
    CATEGORY_NAME NVARCHAR2(50),
    DESCRIPTION NVARCHAR2(200),
    PICTURE NVARCHAR2(50)
);

CREATE TABLE PRODUCTS
(
    ID NUMBER,
    CONSTRAINT PRODUCT_ID PRIMARY KEY (ID),
    PRODUCT_NAME NVARCHAR2(50) NOT NULL,
    SUPPLIER_ID NUMBER,
    CONSTRAINT SUPPLIER_FK FOREIGN KEY(SUPPLIER_ID) REFERENCES SUPPLIERS (ID),
    CATEGORY_ID NUMBER,
    CONSTRAINT CATEGORY_FK FOREIGN KEY(CATEGORY_ID) REFERENCES CATEGORIES (ID),
    QUANTITY_PER_UNIT NUMBER,
    UNIT_PRICE NUMBER(,2),
    UNITS_IN_STOCK NUMBER,
    UNITS_ON_ORDER NUMBER,
    REORDER_LEVEL NUMBER,
    DISCONTINUED NVARCHAR2(50)
);

CREATE TABLE ORDER_DETAILS
(
    ID NUMBER,
    CONSTRAINT ORDER_ID PRIMARY KEY (ID),
    PRODUCT_ID NUMBER,
    CONSTRAINT PRODUCT_FK FOREIGN KEY(PRODUCT_ID) REFERENCES PRODUCTS (ID),
    UNIT_PRICE NUMBER NOT NULL,
    QUANTITY NUMBER NOT NULL,
    DISCOUNT NUMBER NOT NULL
);

